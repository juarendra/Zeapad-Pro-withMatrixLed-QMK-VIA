        //assumes 1 frame prep stage
        void animation_phase(void) {
            switch (get_highest_layer(layer_state)) {
                case 0: 
                    current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
                    oled_write_raw_P(idle[abs((IDLE_FRAMES-1)-current_idle_frame)], ANIM_SIZE);
                    break;
                case 1: 
                    current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
                    oled_write_raw_P(tap[abs((TAP_FRAMES-1)-current_tap_frame)], ANIM_SIZE);
                    break;
                case 2:
                    current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
                    oled_write_raw_P(idle_[abs((IDLE_FRAMES-1)-current_idle_frame)], ANIM_SIZE);
                    break;
                case 3:
                    current_tap_frame = (current_tap_frame + 1) % TAP_FRAMES;
                    oled_write_raw_P(tap_[abs((TAP_FRAMES-1)-current_tap_frame)], ANIM_SIZE);
                    break;
                default:
                    current_idle_frame = (current_idle_frame + 1) % IDLE_FRAMES;
                    oled_write_raw_P(idle[abs((IDLE_FRAMES-1)-current_idle_frame)], ANIM_SIZE);
            }
        }

        // variable animation duration. Don't want this value to get near zero as it'll bug out.
        curr_anim_duration = MAX(ANIM_FRAME_DURATION_MIN, ANIM_FRAME_DURATION_MAX - ANIM_FRAME_RATIO * get_current_wpm());

        if(get_current_wpm() > ANIM_WPM_LOWER) {
            oled_on(); // not essential but turns on animation OLED with any alpha keypress
            if(timer_elapsed32(anim_timer) > curr_anim_duration) {
                anim_timer = timer_read32();
                animation_phase();
            }
            anim_sleep = timer_read32();
        } else {
            if(timer_elapsed32(anim_sleep) > OLED_TIMEOUT) {
                oled_on(); // not essential but turns on animation OLED with any alpha keypress
                if(timer_elapsed32(anim_timer) > curr_anim_duration) {
                    anim_timer = timer_read32();
                    animation_phase();
                }
                anim_sleep = timer_read32();
            } else {
                if(timer_elapsed32(anim_timer) > IDLE_FRAME_DURATION) {
                    oled_on(); // not essential but turns on animation OLED with any alpha keypress
                    if(timer_elapsed32(anim_timer) > curr_anim_duration) {
                        anim_timer = timer_read32();
                        animation_phase();
                    }
                    anim_sleep = timer_read32();
                }
            }
        }
